package com.prakashp007;

public class Main {

    public static void main(String[] args) {
	// write your code here
        //forloop in java
        //syn..int i(initialization),i<100(condtion statement),i++/i--(inc/dec)
        //1 to 100
        for (int i=1;i<=100;i++)
        {
            System.out.println(i);
        }
        //100 to 1
        for (int i=100;i>=1;i--)
        {
            System.out.println(i);
        }
    }
}
